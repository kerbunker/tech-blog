// gets the model and datatypes info from sequelize package and the connected server data
const { Model, DataTypes } = require('sequelize');
// gets data for password encryption
const bcrypt = require('bcrypt');
const sequelize = require('../config/connection');

class User extends Model {
  // allows the encrypted password to be checked to log user in
  checkPassword(loginPw) {
    return bcrypt.compareSync(loginPw, this.password);
  }
}

// model to create a new user
User.init(
  {
    id: {
      type: DataTypes.INTEGER,
      allowNull: false,
      primaryKey: true,
      autoIncrement: true
    },
    username: {
      type: DataTypes.STRING,
      allowNull: false
    },
    email: {
      type: DataTypes.STRING,
      allowNull: false,
      unique: true,
      validate: {
        isEmail: true
      }
    },
    password: {
      type: DataTypes.STRING,
      allowNull: false,
      validate: {
        len: [4]
      }
    }
  },
  {
    hooks: {
      // ensures password is encrupted before data is stored
      async beforeCreate(newUserData) {
        newUserData.password = await bcrypt.hash(newUserData.password, 10);
        return newUserData;
      },

      async beforeUpdate(updatedUserData) {
        updatedUserData.password = await bcrypt.hash(updatedUserData.password, 10);
        return updatedUserData;
      }
    },
    sequelize,
    timestamps: false,
    freezeTableName: true,
    underscored: true,
    modelName: 'user'
  }
);

module.exports = User;